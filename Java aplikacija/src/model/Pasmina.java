package model;

public class Pasmina
{
	private int idPasmina;
	private String naziv;
	
	public Pasmina()
	{
		super();
	}

	public int getIdPasmina()
	{
		return idPasmina;
	}

	public void setIdPasmina(int idPasmina)
	{
		this.idPasmina = idPasmina;
	}

	public String getNaziv()
	{
		return naziv;
	}

	public void setNaziv(String naziv)
	{
		this.naziv = naziv;
	}
}
